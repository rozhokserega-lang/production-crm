create or replace function public.trg_crm_audit_user_roles()
returns trigger
language plpgsql
security definer
set search_path to 'public', 'extensions', 'pg_temp'
as $$
begin
  if tg_op = 'INSERT' then
    perform public.web_audit_log_event(
      'assign_role',
      'crm_user_roles',
      new.user_id::text,
      jsonb_build_object('role', new.role, 'note', new.note)
    );
    return new;
  end if;

  if tg_op = 'UPDATE' then
    if coalesce(old.role, '') is distinct from coalesce(new.role, '')
       or coalesce(old.note, '') is distinct from coalesce(new.note, '') then
      perform public.web_audit_log_event(
        'assign_role',
        'crm_user_roles',
        new.user_id::text,
        jsonb_build_object(
          'before', jsonb_build_object('role', old.role, 'note', old.note),
          'after', jsonb_build_object('role', new.role, 'note', new.note)
        )
      );
    end if;
    return new;
  end if;

  if tg_op = 'DELETE' then
    perform public.web_audit_log_event(
      'remove_role',
      'crm_user_roles',
      old.user_id::text,
      jsonb_build_object('role', old.role, 'note', old.note)
    );
    return old;
  end if;

  return null;
end;
$$;

drop trigger if exists trg_crm_audit_user_roles on public.crm_user_roles;
create trigger trg_crm_audit_user_roles
after insert or update or delete on public.crm_user_roles
for each row
execute function public.trg_crm_audit_user_roles();

create or replace function public.web_get_audit_log(
  p_limit integer default 200,
  p_offset integer default 0,
  p_action text default null
)
returns table (
  id bigint,
  created_at timestamptz,
  actor_user_id uuid,
  actor_db_role text,
  actor_crm_role text,
  action text,
  entity text,
  entity_id text,
  details jsonb
)
language plpgsql
security definer
set search_path to 'public', 'extensions', 'pg_temp'
as $$
declare
  v_limit integer := greatest(1, least(coalesce(p_limit, 200), 1000));
  v_offset integer := greatest(coalesce(p_offset, 0), 0);
  v_action text := nullif(lower(trim(coalesce(p_action, ''))), '');
begin
  perform public.web_require_roles(array['admin', 'manager']);

  return query
  select
    l.id,
    l.created_at,
    l.actor_user_id,
    l.actor_db_role,
    l.actor_crm_role,
    l.action,
    l.entity,
    l.entity_id,
    l.details
  from public.crm_audit_log l
  where v_action is null or l.action = v_action
  order by l.created_at desc, l.id desc
  limit v_limit
  offset v_offset;
end;
$$;

grant execute on function public.web_get_audit_log(integer, integer, text) to authenticated, service_role;

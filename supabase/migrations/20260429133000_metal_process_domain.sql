-- Separate metal process domain: catalog, work items, stage events and RPCs.

create table if not exists public.metal_product_catalog (
  article text primary key,
  name text not null,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.metal_work_items (
  id bigint generated by default as identity primary key,
  article text not null references public.metal_product_catalog(article) on update cascade,
  name text not null,
  week text,
  qty numeric(12,3) not null default 0 check (qty > 0),
  current_stage text not null default 'laser' check (current_stage in ('laser', 'bending', 'welding', 'painting')),
  stage_status text not null default 'queued' check (stage_status in ('queued', 'in_progress', 'paused', 'done')),
  status text not null default 'active' check (status in ('active', 'done', 'cancelled')),
  current_stage_started_at timestamptz,
  created_by uuid null default auth.uid(),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.metal_stage_events (
  id bigint generated by default as identity primary key,
  work_item_id bigint not null references public.metal_work_items(id) on delete cascade,
  stage text not null check (stage in ('laser', 'bending', 'welding', 'painting')),
  action text not null check (action in ('start', 'pause', 'resume', 'done')),
  actor_user_id uuid null default auth.uid(),
  note text,
  event_at timestamptz not null default now()
);

create index if not exists idx_metal_product_catalog_active on public.metal_product_catalog(is_active, article);
create index if not exists idx_metal_work_items_status on public.metal_work_items(status, current_stage, stage_status, created_at desc);
create index if not exists idx_metal_stage_events_item on public.metal_stage_events(work_item_id, event_at);

create or replace function public.trg_metal_process_touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists trg_metal_product_catalog_touch_updated_at on public.metal_product_catalog;
create trigger trg_metal_product_catalog_touch_updated_at
before update on public.metal_product_catalog
for each row
execute function public.trg_metal_process_touch_updated_at();

